#거스름돈 문제 500 100 50 10 무한히 존재, 거스름 돈이 N일때 거스름돈의 최소 갯수 구하기

N=int(input())
cnt=0

coin_type=[500,100,50,10]

for coin in coin_type:
    cnt += N//coin
    N %= coin

print(cnt)

#위 문제는 그리디로 푸는 방법이 정당하다. 그 이유는 가장 큰 것이 최적의 방법이다.
#최적의 방법인 이유는 큰수가 바로 작은수의 배수 이기 때문이다. 이처럼 정당성을 찾아야 한다. 그리디 문제에서는
#500 400 50 10 이면 이 정당성이 깨진다. 생각해야한다.
